#comparison operators
x = 9
y = 3

print(x==y) #true if x=y, false otherwise
print(x!=y) #true if x is not =y, false otherwise
print(x>y) #true if x>y, false otherwise
print(x<y) #true if x<y, false otherwise
print(x>=y) #true if x greater or equal to y, false otherwise
